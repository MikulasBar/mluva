use crate::data_type::DataType;

#[derive(Debug, Clone, PartialEq)]
pub enum Token {
    EOL,
    Assign,
    Plus,
    Minus,
    Asterisk,
    Slash,
    Eq,
    Neq,
    Percentage,
    Print,
    If,
    Let,
    // Else,
    While,
    ParenL,
    ParenR,
    BracketL,
    BracketR,
    BraceL,
    BraceR,
    EOF,
    DataType(DataType),
    Ident(String),
    StringLiteral(String),
    Num(u64),
    Bool(bool),
}